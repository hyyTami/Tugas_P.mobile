void main() {
    var x = 10;
   print("x = 10 -> ${x}");//Operator penugasan

   x += 15;
   print("x += 15 -> ${x}");//Tambahkan dan tetapkan gabungan

   x -= 10;
   print("x -= 10 -> ${x}");//Kurangi dan tugaskan

   x *= 10;
   print("x *= 10 -> ${x}");//Lipat gandakan dan tetapkan

   x ~/= 5;
   print("x /= 5 -> ${x}");//Bagilah dan tugaskan

   x %= 7;
   print("x %= 7 -> ${x}");//Modulus dan tetapkan

   x <<= 2;
   print("x <<= 2 -> ${x}");//Shift kiri dan tetapkan

   x >>= 3;
   print("x >>= 3 -> ${x}");//shift kanandan tetapkan

   x &= 2;
   print("x &= 2 -> ${x}");//Bitwise DAN tetapkan

   x ^= 5;
   print("x ^= 5 -> ${x}");//OR eksklusif Bitwise dan tetapkan

   x |= 10;
   print("x |= 10 -> ${x}");//Bitwise inklusif OR dan tetapkan
}